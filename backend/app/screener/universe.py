async def refresh_universe(pool: asyncpg.Pool) -> dict[str, int]:
    """Upsert seed members into equities + universe_members; flag departures."""
    today = datetime.now(UTC).date()
    added = 0
    async with pool.acquire() as conn, conn.transaction():
        for symbol, name, sector, universe in UNIVERSE_SEED:
            eq = await conn.fetchrow(
                """
                INSERT INTO equities (symbol, name, gics_sector)
                VALUES ($1, $2, $3)
                ON CONFLICT (symbol) DO UPDATE
                  SET name = COALESCE(NULLIF(equities.name, ''), EXCLUDED.name),
                      gics_sector = COALESCE(equities.gics_sector, EXCLUDED.gics_sector)
                RETURNING id
                """,
                symbol,
                name,
                sector,
            )
            assert eq is not None
            result = await conn.execute(
                """
                INSERT INTO universe_members (equity_id, universe, is_current,
                                              first_seen, last_seen)
                VALUES ($1, $2, TRUE, $3, $3)
                ON CONFLICT (equity_id, universe) DO UPDATE
                  SET is_current = TRUE, last_seen = $3
                """,
                eq["id"],
                universe,
                today,
            )
            if result.startswith("INSERT"):
                added += 1
        stale = await conn.execute(
            "UPDATE universe_members SET is_current = FALSE WHERE last_seen < $1",
            today,
        )
    marked = int(stale.split()[-1]) if stale else 0
    total = len({s for s, *_ in UNIVERSE_SEED})
    logger.info("[screener] universe refresh: +%d, stale %d, total %d", added, marked, total)
    return {"added": added, "marked_stale": marked, "total": total}


async def universe_symbols(pool: asyncpg.Pool, universe: str | None = None) -> list[dict[str, Any]]:
    """Current members (deduped across universes unless one is requested)."""
    where = "m.is_current" + (" AND m.universe = $1" if universe else "")
    args = [universe] if universe else []
    rows = await pool.fetch(
        f"""
        SELECT DISTINCT ON (e.symbol) e.id AS equity_id, e.symbol, e.name,
               e.gics_sector AS sector
        FROM universe_members m JOIN equities e ON e.id = m.equity_id
        WHERE {where} ORDER BY e.symbol
        """,  # noqa: S608 — `where` is built from constants only
        *args,
    )
    return [dict(r) for r in rows]
